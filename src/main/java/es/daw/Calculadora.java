package es.daw;

public class Calculadora {

  public static int sumar(int a, int b) {

    return a + b;
  }

  public static int restar(int a, int b) {

    return a - b;
  }

  public static int division(int a, int b) {

    return a / b;
  }

  public static int producto(int a, int b) {

    return a * b;
  }
}